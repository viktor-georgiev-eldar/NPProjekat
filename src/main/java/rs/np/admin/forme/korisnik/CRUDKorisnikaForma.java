    private boolean validirajPolja() {
        if (txtIme.getText().length() < 2) {
            return false;
        }
        if (txtPrezime.getText().length() < 2) {
            return false;
        }
        if (txtBrojTelefona.getText().length() < 6) {
            return false;
        }
        if (txtUsername.getText().length() < 2) {
            return false;
        }
        if (String.valueOf(txtPassword.getPassword()).length() < 3) {
            return false;
        }
        return true;
    }

    public void izmenaKorisnika(Korisnik k) {

        lblNaslov.setText("Izmena korisnika");
        btnDodaj.setText("Izmeni");
        txtIme.setText(k.getIme());
        txtBrojTelefona.setText(k.getTelefon());
        txtPrezime.setText(k.getPrezime());
        txtUsername.setText(k.getUsername());
        txtPassword.setText(k.getPassword());
        this.setTitle("Izmena korisnika");
        this.trenutniKorisnik=k;
        
    }

