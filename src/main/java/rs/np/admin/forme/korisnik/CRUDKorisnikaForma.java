    private void btnVidiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVidiActionPerformed
        ButtonModel bModel = btnVidi.getModel();
        bModel.addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent e) {
                if (bModel.isPressed()) {
                    txtPassword.setEchoChar((char) 0);
                } else if (!bModel.isPressed()) {
                    txtPassword.setEchoChar('*');
                }
            }
        });

    }//GEN-LAST:event_btnVidiActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        if (!validirajPolja()) {
            JOptionPane.showMessageDialog(this, "Losi podaci");
            return;
        }

        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String brojTelefona = txtBrojTelefona.getText();
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        
        //SLUCAJ GDE SE FORMA KORISTI ZA DODAVANJE KORISNIKA
        if (btnDodaj.getText().equals("Dodaj")) {
            int znak=0;
            try {
                znak = ControllerAdministrator.getInstance().dodajKorisnika(ime, prezime, brojTelefona, username, password);
            } catch (SQLException ex) {
                Logger.getLogger(CRUDKorisnikaForma.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (znak == 1) {
                txtIme.setText("");
                txtBrojTelefona.setText("");
                txtPassword.setText("");
                txtPrezime.setText("");
                txtUsername.setText("");
                JOptionPane.showMessageDialog(this, "Korisnik " + ime + " " + prezime + " uspesno dodat");
            } else {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti korisnika.\n (Proverite da li vec postoji korisnik sa istim korisnickim imenom)","Greska",JOptionPane.WARNING_MESSAGE);      
            }
        }
        //SLUCAJ GDE SE FORMA KORISTI ZA IZMENU KORISNIKA
        else{
            if(trenutniKorisnik==null){
                JOptionPane.showMessageDialog(this, "Tekuci korisnik nije inicijalizovan");
                return;
            }
            int znak=0;
            Korisnik k=new Korisnik(trenutniKorisnik.getKorisnikId(), username, password, ime, prezime, brojTelefona, TipKorisnika.KORISNIK,false);
            try {
                znak=ControllerAdministrator.getInstance().izmeniKorisnika(k);
            } catch (Exception ex) {
                Logger.getLogger(CRUDKorisnikaForma.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(znak>=1){
                JOptionPane.showMessageDialog(this, "Korisnik " + ime + " " + prezime + " uspesno izmenjen");
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti korisnika","Greska",JOptionPane.WARNING_MESSAGE);
                this.dispose();
            }
        }
        
        
        
    }//GEN-LAST:event_btnDodajActionPerformed

