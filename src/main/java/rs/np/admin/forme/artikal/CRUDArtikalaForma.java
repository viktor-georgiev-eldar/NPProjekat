    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (!validirajPolja()) {
            JOptionPane.showMessageDialog(this, "Unete vrednosti nisu dobre");
            return;
        }

        String naziv = txtNaziv.getText();
        String opis = txtOpis.getText();
        double cena = Double.parseDouble(txtCena.getText());
        
        if (btnDodaj.getText().equals("Dodaj")) {
            int znak=0;
            try {
                znak = ControllerAdministrator.getInstance().dodajArtikal(naziv, opis, cena);
            } catch (Exception ex) {
                Logger.getLogger(CRUDArtikalaForma.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (znak == 1) {
                txtCena.setText("");
                txtNaziv.setText("");
                txtOpis.setText("");
                JOptionPane.showMessageDialog(this, "Artikal " + naziv + " je uspesno dodat.");
            } else {
                JOptionPane.showMessageDialog(this,"Sistem ne moze da zapamti artikal" ,"Greska",JOptionPane.WARNING_MESSAGE);
            }
        }
        else if(btnDodaj.getText().equals("Izmeni")){
            if(artikal==null){
                JOptionPane.showMessageDialog(this, "Artikal mora biti inicijalizovan");
                return;
            }
            
            int znak=0;                          
            try {
                znak = ControllerAdministrator.getInstance().izmeniArtikal(new Artikal(artikal.getArtikalId(), naziv,opis,cena));
            } catch (Exception ex) {
                Logger.getLogger(CRUDArtikalaForma.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(znak>=0){
                JOptionPane.showMessageDialog(this, "Atikal "+naziv+" je uspesno izmenjen");
            }else{
                JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti artikal","Greska",JOptionPane.WARNING_MESSAGE);
            }
            this.dispose();
        }
    }//GEN-LAST:event_btnDodajActionPerformed

