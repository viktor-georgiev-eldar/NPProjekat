    private boolean validirajPolja() {
        if (txtOpis.getText().length() < 3) {
            return false;
        }
        if (txtNaziv.getText().length() < 3) {
            return false;
        }
        try {
            double cena = Double.parseDouble(txtCena.getText());
        } catch (NumberFormatException e) {
            return false;
        }

        return true;
    }

    public void izmenaArtikala(Artikal a){
        
        lblNaslov.setText("Izmena artikla");
        txtNaziv.setText(a.getNaziv());
        txtOpis.setText(a.getOpis());
        txtCena.setText(String.valueOf(a.getCena()));
        btnDodaj.setText("Izmeni");
        this.setTitle("Izmena Artikla");
        artikal=a;
    }
    
